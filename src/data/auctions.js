export function addAuction(data) {
  const hasAuth = data.authAuthority?.trim() || data.authDocName?.trim();
  const auction = {
    id: _nextId++,
    name: data.name,
    category: data.category || 'Custom',
    condition: data.condition || null,
    description: data.description || '',
    duration: Math.round(parseFloat(data.durationHours) * 60 * 60 * 1000),
    bidCount: 0,
    estimate: `${parseFloat(data.minBid).toFixed(2)} SOL min`,
    icon: data.icon || '🏷️',
    visual: 'purple',
    auctionType: data.auctionType,
    minBid: parseFloat(data.minBid) || 0,
    reservePrice: data.reservePrice ? parseFloat(data.reservePrice) : null,
    reserveMet: false,
    sellerWallet: data.sellerWallet || null,
    authenticity: hasAuth ? {
      verified: true,
      id: `GVL-${Date.now().toString(36).toUpperCase()}`,
      type: data.authType || 'Physical Item',
      authority: data.authAuthority || '',
      docRef: `DOC-${Math.random().toString(36).slice(2, 8).toUpperCase()}`,
      docName: data.authDocName || '',
      smartContract: data.authContract?.trim() || null,
      verifiedAt: new Date().toISOString().split('T')[0],
    } : { verified: false },
    fakeBids: [],
  };
  AUCTIONS.push(auction);
  window.dispatchEvent(new CustomEvent('gavel:auction-added'));
  return auction;
}

export function getAuction(id) {
  return AUCTIONS.find((a) => a.id === Number(id));
}

export function getTimeLeft(auction) {
  const elapsed = Date.now() - APP_START;
  return Math.max(0, auction.duration - elapsed);
}
